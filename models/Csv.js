const mongoose = require('mongoose');

const CsvSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true
  },
  email: {
    type: String,
    required: true
  },
  password: {
    type: String,
    required: true
  },
  date: {
    type: Date,
    default: Date.now
  }
});

const Csv = mongoose.model('CsvData', CsvSchema);

module.exports = Csv;
